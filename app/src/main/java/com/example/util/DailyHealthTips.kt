    fun getTipForToday(): HealthTip {
        val calendar = Calendar.getInstance()
        val dayOfMonth = calendar.get(Calendar.DAY_OF_MONTH)
        // Ensure within bounds 1..31
        val targetDay = if (dayOfMonth in 1..31) dayOfMonth else 1
        return tipsList.firstOrNull { it.dayIndex == targetDay } ?: tipsList.first()
    }
